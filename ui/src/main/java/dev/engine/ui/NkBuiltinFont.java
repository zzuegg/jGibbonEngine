    private static final int GLYPH_W = 8;
    private static final int GLYPH_H = 8;
    private static final int CHARS_PER_ROW = 16;
    private static final int FIRST_CHAR = 32;
    private static final int LAST_CHAR = 126;
    private static final int CHAR_COUNT = LAST_CHAR - FIRST_CHAR + 1;
    private static final int ROWS = (CHAR_COUNT + CHARS_PER_ROW - 1) / CHARS_PER_ROW;

    // Atlas dimensions: extra 2 pixels for the white patch
    private static final int ATLAS_W = CHARS_PER_ROW * GLYPH_W;
    private static final int ATLAS_H = ROWS * GLYPH_H + 2;

    private final float scale;
    private final byte[] atlas;
    private final Glyph[] glyphs = new Glyph[CHAR_COUNT];

    // White pixel UV in the atlas (bottom-right 2x2 patch)
    private final float whiteU;
    private final float whiteV;

    public NkBuiltinFont() {
        this(1);
    }

    public NkBuiltinFont(int scale) {
        this.scale = scale;
        this.atlas = bakeAtlas();

        // White pixel is at bottom-right corner
        this.whiteU = (ATLAS_W - 1.0f) / ATLAS_W;
        this.whiteV = (ATLAS_H - 1.0f) / ATLAS_H;

        // Build glyph table
        for (int i = 0; i < CHAR_COUNT; i++) {
            int col = i % CHARS_PER_ROW;
            int row = i / CHARS_PER_ROW;
            float u0 = (float) (col * GLYPH_W) / ATLAS_W;
            float v0 = (float) (row * GLYPH_H) / ATLAS_H;
            float u1 = (float) (col * GLYPH_W + GLYPH_W) / ATLAS_W;
            float v1 = (float) (row * GLYPH_H + GLYPH_H) / ATLAS_H;
            glyphs[i] = new Glyph(GLYPH_W * scale, u0, v0, u1, v1,
                    0, 0, GLYPH_W * scale, GLYPH_H * scale);
        }
    }

    private byte[] bakeAtlas() {
        byte[] pixels = new byte[ATLAS_W * ATLAS_H * 4];

        for (int i = 0; i < CHAR_COUNT && i < FONT_DATA.length; i++) {
            int col = i % CHARS_PER_ROW;
            int row = i / CHARS_PER_ROW;
            int baseX = col * GLYPH_W;
            int baseY = row * GLYPH_H;

            byte[] glyph = FONT_DATA[i];
            for (int gy = 0; gy < GLYPH_H; gy++) {
                int bits = glyph[gy] & 0xFF;
                for (int gx = 0; gx < GLYPH_W; gx++) {
                    boolean set = (bits & (1 << gx)) != 0;
                    int px = baseX + gx;
                    int py = baseY + gy;
                    int idx = (py * ATLAS_W + px) * 4;
                    byte v = set ? (byte) 0xFF : 0;
                    pixels[idx] = v;
                    pixels[idx + 1] = v;
                    pixels[idx + 2] = v;
                    pixels[idx + 3] = v;
                }
            }
        }

        // White 2x2 patch at bottom-right for solid color rendering
        int whiteY = ROWS * GLYPH_H;
        for (int dy = 0; dy < 2; dy++) {
            for (int dx = 0; dx < 2; dx++) {
                int px = ATLAS_W - 2 + dx;
                int py = whiteY + dy;
                int idx = (py * ATLAS_W + px) * 4;
                pixels[idx] = (byte) 0xFF;
                pixels[idx + 1] = (byte) 0xFF;
                pixels[idx + 2] = (byte) 0xFF;
                pixels[idx + 3] = (byte) 0xFF;
            }
        }

        return pixels;
    }

    @Override
    public float height() {
        return GLYPH_H * scale;
    }

    @Override
    public float textWidth(String text) {
        return text.length() * GLYPH_W * scale;
    }

    @Override
    public int atlasWidth() {
        return ATLAS_W;
    }

    @Override
    public int atlasHeight() {
        return ATLAS_H;
    }

    @Override
    public byte[] atlasData() {
        return atlas;
    }

    @Override
    public Glyph glyph(int codepoint) {
        int index = codepoint - FIRST_CHAR;
        if (index < 0 || index >= CHAR_COUNT) return null;
        return glyphs[index];
    }

    @Override
    public float whiteU() {
        return whiteU;
    }

    @Override
    public float whiteV() {
        return whiteV;
    }
